#!/usr/bin/env python3
"""
knowledge_extractor.py — Knowledge Extraction and RAG Indexing

Reads study queue items, generates structured knowledge notes, and optionally
indexes them into a vector store for semantic search.

This script bridges the gap between raw Airlock records and a searchable
personal knowledge base.

Usage:
    # Extract knowledge from all unindexed queue items
    python knowledge_extractor.py --mode extract

    # Extract and index into RAG
    python knowledge_extractor.py --mode index

    # Search indexed knowledge
    python knowledge_extractor.py --mode search --query "vertex AI quota"

    # Show extraction stats
    python knowledge_extractor.py --mode stats

Key design decisions:
- Extraction is decoupled from indexing: you can extract without RAG configured.
- Each extracted document has a stable ID derived from the study item ID, so
  re-indexing is idempotent (upsert, not insert).
- The RAG backend is pluggable: Chroma (local), Weaviate (hosted), or pgvector.
- If the LLM call fails during extraction, the script writes a partial record
  and marks it for retry rather than failing the whole batch.
"""

